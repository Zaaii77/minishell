#include "../../inc/libft.h"

char	*ft_strrchr(const char *s, int a)
{
	int		i;
	char	c;

	i = ft_strlen(s);
	c = (char)a;
	if (c == '\0')
		return ((char *)&s[i]);
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
